		ga v2{v[2].x, v[2].y, v[2].z};

		glBegin(GL_TRIANGLES);
		    PutNormal( v0, v1, v2 );
		    glVertex3f( v[0].x, v[0].y, v[0].z );
		    PutNormal( v0, v1, v2 );
		    glVertex3f( v[1].x, v[1].y, v[1].z );
		    PutNormal( v0, v1, v2 );
		    glVertex3f( v[2].x, v[2].y, v[2].z );
