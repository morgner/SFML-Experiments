class CGlQuader
    {
    public:

    bool operator()(float const & x, float const & y, float const & z,
                    SMaterial const & c1 , SMaterial const & c2 , SMaterial const & c3 ,
                    SMaterial const & c4 , SMaterial const & c5 , SMaterial const & c6   )
        {
        glBegin(GL_QUADS);

	    glPushMatrix();
		  c1();
		    glVertex3f(-x, -y, -0);
		    glVertex3f(-x,  y, -0);
		    glVertex3f( x,  y, -0);
		    glVertex3f( x, -y, -0);
	    glPopMatrix();

	    glPushMatrix();
		  c2();
		    glVertex3f(-x, -y,  z*2);
		    glVertex3f(-x,  y,  z*2);
		    glVertex3f( x,  y,  z*2);
		    glVertex3f( x, -y,  z*2);
	    glPopMatrix();

	    glPushMatrix();
		  c3();
		    glVertex3f(-x, -y, -0);
		    glVertex3f(-x,  y, -0);
		    glVertex3f(-x,  y,  z*2);
		    glVertex3f(-x, -y,  z*2);
	    glPopMatrix();

	    glPushMatrix();
		  c4();
		    glVertex3f( x, -y, -0);
		    glVertex3f( x,  y, -0);
		    glVertex3f( x,  y,  z*2);
		    glVertex3f( x, -y,  z*2);
	    glPopMatrix();

	    glPushMatrix();
		  c5();
		    glVertex3f(-x, -y,  z*2);
		    glVertex3f(-x, -y, -0);
		    glVertex3f( x, -y, -0);
		    glVertex3f( x, -y,  z*2);
	    glPopMatrix();

	    glPushMatrix();
		  c6();
		    glVertex3f(-x,  y,  z*2);
		    glVertex3f(-x,  y, -0);
		    glVertex3f( x,  y, -0);
		    glVertex3f( x,  y,  z*2);
	    glPopMatrix();

        glEnd();

        return true;
        }
    };

